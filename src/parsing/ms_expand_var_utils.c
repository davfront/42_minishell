#include "minishell.h"

int	ms_is_char_print(char c)
{
	if (c == '%' || c == '^' || \
		c == '+' || c == '=' || \
		c == ' ' || c == '~' || \
		c == ':' || c == '\"' || \
		c == '\'' || c == '\0')
		return (1);
	return (0);
}

int	ms_is_char_nprint(char c)
{
	if (c == '!' || c == '@' || c == '#' || c == '&' || \
		c == '*' || c == '(' || c == ')' || c == '-' || \
		c == '_' || c == '{' || c == '}' || c == '[' || \
		c == ']' || c == ';' || c == ',' || c == '.' || \
		c == '|' || c == '/' || c == '|' || c == '$' || c == '\\')
		return (1);
	return (0);
}
